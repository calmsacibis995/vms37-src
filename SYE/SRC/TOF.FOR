	INTEGER		PAGE

	CHARACTER*17	TIME

	CHARACTER*1	LINE_FEED

	CHARACTER*1	THROW_PAGE

	CHARACTER*13	HERALD1

	CHARACTER*19	HERALD2

	CHARACTER*9	HERALD3

	CHARACTER*5	HERALD4

	DATA	LINE_FEED	/' '/

	DATA	THROW_PAGE	/'1'/



	DATA	HERALD1	/'V A X / V M S'/

	DATA	HERALD2	/'SYSTEM ERROR REPORT'/

	DATA	HERALD3	/'COMPILED '/

	DATA	HERALD4	/'PAGE '/




	ENTRY FRCTOF (LUN)



	IF (LINES .EQ. 1 .AND. .NOT. PRINTER) THEN

	WRITE(LUN,140) LINE_FEED
140	FORMAT(A1,/)

	ELSE IF (PRINTER) THEN

	WRITE(LUN,145) THROW_PAGE
145	FORMAT(A1)

	ELSE IF (FORMS) THEN

	WRITE(LUN,150) (LINE_FEED,I = LINES,69)
150	FORMAT(A1)
	ENDIF

	IF (FORMS) THEN

	CALL SYS$ASCTIM (,TIME,,%VAL(0))

	PAGE = PAGE + 1

	IF (PAGE .EQ. 1000) PAGE = 1

 	WRITE(LUN,155) HERALD1,HERALD2,HERALD3,TIME,HERALD4,PAGE
155	FORMAT('+',A13,T23,A19,T48,A9,A,/,
	1 T65,A5,I3,'.',/)

	LINES = 6

	RETURN
	ENDIF

	LINES = 3

	RETURN

	END
