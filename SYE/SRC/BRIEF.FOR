	Include	'SRC$:SYECOM.FOR /nolist'


	BYTE		LUN

	BYTE		NUMREG

	INTEGER*2	UNIT

	INTEGER*4	REGISTERS(0:NUMREG-1)

	INTEGER*4	FIELD

	INTEGER*4	COMPRESS4

	INTEGER*4	COMPRESSC

	CHARACTER*(*)	REG_HERALD(0:NUMREG-1)

	CHARACTER*3	NAME




	ENTRY BRIEF16 (LUN,NUMREG,REGISTERS,REG_HERALD,NAME,UNIT)




	IF (NUMREG .GT. 12) NUMREG = 12

	CALL LINCHK (LUN,3)

	WRITE(LUN,10) (REG_HERALD(I),I = 0,NUMREG - 1)
10	FORMAT('0',<NUMREG>(:,T<(I*5)+8>,A<COMPRESSC (REG_HERALD(I))>),/)


	If (cryptk_flag) then 
	Return
	Endif


	ENTRY BRIEF16_A (LUN,NUMREG,REGISTERS)




	IF (NUMREG .GT. 12) NUMREG = 12

	CALL LINCHK (LUN,1)

	WRITE(LUN,20) (JIAND(REGISTERS(I),'FFFF'X),I = 0,NUMREG - 1)
20	FORMAT(' ',<NUMREG>(:,T<(I*5)+8>,Z4.4))

	RETURN



	ENTRY BRIEF32 (LUN,NUMREG,REGISTERS,REG_HERALD,NAME,UNIT)



	IF (NUMREG .GT. 7) NUMREG = 7

	CALL LINCHK (LUN,3)

	WRITE(LUN,30) (REG_HERALD(I),I = 0,NUMREG - 1)
30	FORMAT('0',<NUMREG>(:,T<(I*9)+8>,
	1 A<COMPRESSC (REG_HERALD(I))>),/)


	If (cryptk_flag) then
	Return
	Endif


	ENTRY BRIEF32_A (LUN,NUMREG,REGISTERS)



	IF (NUMREG .GT. 7) NUMREG = 7

	CALL LINCHK (LUN,1)

	WRITE(LUN,40) (REGISTERS(I),I = 0,NUMREG - 1)
40	FORMAT(' ',<NUMREG>(:,T<(I*9)+8>,Z8.8))

	RETURN

	END
