	DATA	FAO_CONTROL	/'!AF'/

	DATA	DATA1_LEN	/4/


	IF (ICOUNT .GT. 0) THEN

	DO 20,I = 1,MIN(127,ICOUNT)

	CALL LINCHK (LUN,2)

	WRITE(LUN,5) I,DATA1(I)
5	FORMAT(' ',T8,'LONGWORD ',I<COMPRESS4 (I)>,'.',T24,Z8.8)

	IF (LIB$SCOPY_R_DX(%REF(DATA1_LEN),%REF(DATA1(I)),DATA2)) THEN

	IF (SYS$FAO(FAO_CONTROL,,DATA3,%VAL(4),%REF(DATA2))) THEN

	WRITE(LUN,10) DATA3
10	FORMAT(' ',T40,'/',A4,'/')
	ENDIF
	ENDIF

20	CONTINUE
	ENDIF

	RETURN

